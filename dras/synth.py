# ---------------------------------------------------------------------------
# Masking procedure: chain → training examples
# ---------------------------------------------------------------------------

def _step_text(step) -> str:
    return step["text"] if isinstance(step, dict) else step


def _step_support(step) -> list[str] | None:
    """Return explicit support premises if annotated, else None."""
    if isinstance(step, dict):
        return step.get("support")
    return None


def build_chains() -> list[dict]:
    """
    Convert all chains into training examples.

    Unannotated steps (plain strings): full-context masking —
      premises = seeds + all prior steps
    Annotated steps (dicts with 'support'): oracle masking —
      premises = support premises only (tightest possible signal)
    Oracle examples are generated twice to upweight targeted patterns.
    """
    from dras.preprocess import _format
    from dras.synth_extended import EXT_CHAINS
    examples = []

    for chain in CHAINS:
        seeds = chain["seeds"]
        goal = chain["goal"]
        accumulated = list(seeds)
        for step in chain["steps"]:
            text = _step_text(step)
            examples.append({"text": _format(goal, accumulated, text)})
            accumulated.append(text)

    for chain in ANNOTATED_CHAINS:
        seeds = chain["seeds"]
        goal = chain["goal"]
        accumulated = list(seeds)
        for step in chain["steps"]:
            text = _step_text(step)
            support = _step_support(step)
            if support:
                # oracle example — model sees only the 2-3 directly supporting premises
                oracle = _format(goal, support, text)
                examples.append({"text": oracle})
                examples.append({"text": oracle})   # upweight x2: these are the key patterns
            full_ctx = _format(goal, accumulated, text)
            repeat = 1
            for _ in range(repeat):
                examples.append({"text": full_ctx})
            accumulated.append(text)

    # extended full-context chains (~20 per pattern, diverse domains)
    for chain in EXT_CHAINS:
        seeds = chain["seeds"]
        goal = chain["goal"]
        accumulated = list(seeds)
        for step in chain["steps"]:
            text = _step_text(step)
            examples.append({"text": _format(goal, accumulated, text)})
            accumulated.append(text)

    return examples


def build_retriever_pairs() -> list[tuple[str, str]]:
    """
    Returns (query, positive_premise) pairs for contrastive retriever training.

    The retriever needs to learn: for a given goal, ALL premises in that chain
    are potentially relevant — not just the most goal-similar ones.

    For annotated chains we also emit (goal, support_premise) pairs with higher
    specificity: these teach the retriever to surface exactly the premises needed
    for each derivation step.
    """
    from dras.synth_extended import EXT_CHAINS
    pairs: list[tuple[str, str]] = []

    # Unannotated: every seed and derived step is a positive for the goal
    for chain in CHAINS:
        goal = chain["goal"]
        for premise in chain["seeds"]:
            pairs.append((goal, premise))
        for step in chain["steps"]:
            pairs.append((goal, _step_text(step)))

    # Annotated: support premises are high-quality positives
    for chain in ANNOTATED_CHAINS:
        goal = chain["goal"]
        for premise in chain["seeds"]:
            pairs.append((goal, premise))
        for step in chain["steps"]:
            pairs.append((goal, _step_text(step)))
            support = _step_support(step)
            if support:
                for sp in support:
                    # emit twice: support premises are the most valuable training signal
                    pairs.append((goal, sp))
                    pairs.append((goal, sp))

    # Extended chains — entry-point seeds get extra pairs so retriever learns
    # to surface them even when they are semantically distant from the goal.
    for chain in EXT_CHAINS:
        goal = chain["goal"]
        seeds = chain["seeds"]
        for premise in seeds:
            pairs.append((goal, premise))
        for step in chain["steps"]:
            pairs.append((goal, _step_text(step)))
        # Upweight the first seed (entity/entry-point fact) to fix retrieval
        # of goal-distant premises in long chains
        if seeds:
            pairs.append((goal, seeds[-1]))  # entity fact is typically last

    return pairs
