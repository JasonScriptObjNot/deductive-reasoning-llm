"""
Synthetic multi-step chain training data.

Each chain defines: seeds (given premises), goal (final conclusion),
and steps (ordered intermediate derivations, last one == goal).

Training examples are generated by the masking procedure:
  for step k: premises = seeds + steps[0..k-1], target = steps[k]

This teaches the model to produce ONE valid intermediate step at a time,
not jump directly to the conclusion — the key capability gap in FOLIO-only training.

Patterns covered:
  modus_ponens_chain      — forward chaining A→B→C→...→Goal
  modus_tollens           — contrapositive instantiation
  contrapositive_chain    — chained modus tollens, working backwards
  disjunctive_syllogism   — A∨B, ¬A ∴ B
  hypothetical_syllogism  — P→Q, Q→R ∴ P→R (with instantiation)
  mixed                   — combinations of the above
"""

